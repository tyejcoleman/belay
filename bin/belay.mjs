#!/usr/bin/env node
// belay — always-on goal loop for Claude Code (official surfaces only).
// Reads Keyoku goal state and tokenroom budget state from their files; hooks never spawn
// their processes, never run probes, never touch credentials, never network. The ONE
// spawn exception (ADR-10): loop create/disarm talk to keyoku through keyoku's own
// registered server process, at MCP-call latency only — never at hook latency.
//
// Round-0 note (docs/DESIGN.md §6.0): dispatch below is COMPLETE for the SOTA surface —
// mcp, loop create/list/pause/resume/disarm, propose, hook session-start. Fill agents
// implement the imported modules only and do not edit this file. The loop/propose CLI
// verbs are thin wrappers over the SAME frozen handlers the MCP tools call (§2.3
// discipline: one composition/handler layer, so CLI and MCP can never drift).

const [cmd, ...argv] = process.argv.slice(2);

/** Tiny flag parser: --key value / --key (boolean) / positionals in `_`. */
function parseFlags(args) {
  const out = { _: [] };
  for (let i = 0; i < args.length; i++) {
    const a = args[i];
    if (typeof a === 'string' && a.startsWith('--')) {
      const key = a.slice(2).replace(/-/g, '_');
      const next = args[i + 1];
      if (next !== undefined && !String(next).startsWith('--')) {
        out[key] = next;
        i++;
      } else out[key] = true;
    } else out._.push(a);
  }
  return out;
}

const printJSON = (r) => console.log(JSON.stringify(r, null, 2));

switch (cmd) {
  case 'hook': {
    // Never-crash rule at the choke point (ADR-4, tokenroom ADR-5 spirit): whatever a
    // hook hits — corrupted state files, malformed stdin, a keyoku layout change — it
    // degrades to silence. A hook must never break the harness or wedge a session.
    try {
      if (argv[0] === 'stop') await (await import('../src/stop.mjs')).hookStop();
      else if (argv[0] === 'pre-tool-use') await (await import('../src/gate.mjs')).hookPreToolUse();
      else if (argv[0] === 'session-start') await (await import('../src/session-start.mjs')).hookSessionStart();
      // unknown hook events exit silently
    } catch {
      process.exitCode = 0;
    }
    break;
  }
  case 'mcp':
    // stdio MCP server (docs/DESIGN.md §2) — registered via `claude mcp add … belay mcp`.
    await (await import('../src/mcp.mjs')).mcpServe(argv);
    break;
  case 'loop': {
    const sub = argv[0];
    const f = parseFlags(argv.slice(1));
    if (sub === 'create') {
      const { loopCreate } = await import('../src/loops.mjs');
      let criteria, constraints;
      try {
        if (f.criteria !== undefined) criteria = JSON.parse(f.criteria);
        if (f.constraints !== undefined) constraints = JSON.parse(f.constraints);
      } catch (e) {
        console.error(`belay: --criteria/--constraints must be valid JSON (${e.message})`);
        process.exitCode = 2;
        break;
      }
      printJSON(
        await loopCreate({
          goal: typeof f.goal === 'string' ? f.goal : f._[0],
          objective: typeof f.objective === 'string' ? f.objective : undefined,
          criteria,
          constraints,
          maxIterations: f.max_iterations !== undefined ? Number(f.max_iterations) : undefined,
          confirm_autonomous: f.confirm_autonomous === true,
          session_id: typeof f.session_id === 'string' ? f.session_id : undefined,
          cwd: typeof f.cwd === 'string' ? f.cwd : process.cwd(),
          proposal_id: typeof f.proposal_id === 'string' ? f.proposal_id : undefined,
        })
      );
    } else if (sub === 'list') {
      printJSON((await import('../src/compose.mjs')).buildLoopList());
    } else if (sub === 'pause') {
      printJSON((await import('../src/loops.mjs')).loopPause({ goal: f._[0] ?? f.goal, note: typeof f.note === 'string' ? f.note : undefined }));
    } else if (sub === 'resume') {
      printJSON((await import('../src/loops.mjs')).loopResume({ goal: f._[0] ?? f.goal }));
    } else if (sub === 'disarm') {
      printJSON(await (await import('../src/loops.mjs')).loopDisarm({ goal: f._[0] ?? f.goal }));
    } else {
      console.error(`belay loop <create|list|pause|resume|disarm> — see \`belay\` for usage`);
      process.exitCode = 2;
    }
    break;
  }
  case 'propose': {
    const f = parseFlags(argv);
    const propose = await import('../src/propose.mjs');
    if (typeof f.dismiss === 'string' && f.dismiss) printJSON(propose.dismiss(f.dismiss));
    else printJSON(propose.scan());
    break;
  }
  case 'install':
    (await import('../src/install.mjs')).install(argv);
    break;
  case 'bundle':
    (await import('../src/bundle.mjs')).bundle(argv);
    break;
  case 'uninstall':
    (await import('../src/install.mjs')).uninstall(argv);
    break;
  case 'doctor':
    (await import('../src/doctor.mjs')).doctor(argv);
    break;
  case 'status':
    (await import('../src/status.mjs')).status(argv);
    break;
  default:
    console.log(`belay — always-on goal loop for Claude Code (reads Keyoku goals + tokenroom budget)

usage:
  belay bundle [--dry-run] [--config-dir <dir>] [--tokenroom <path>]   wire the whole autonomous stack (tokenroom + keyoku + belay) in one command
  belay install [--dry-run] [--config-dir <dir>]   register the Stop + PreToolUse + SessionStart hooks (additive; preserves existing hooks)
  belay uninstall [--config-dir <dir>]             remove only belay's entries
  belay status                                     current focused goal + would-block verdict + counters
  belay doctor                                     full-stack health + keyoku layout self-check, tokenroom presence, hook registration, config validity
  belay mcp                                        stdio MCP server (belay_status, belay_loop_*, belay_propose) — registered by install
  belay loop create [--goal <slug|id>] [--objective <text> --criteria <json>]
                    [--constraints <json>] [--max-iterations <n>] [--confirm-autonomous]
                    [--session-id <id>] [--cwd <dir>] [--proposal-id <id>]
                                                   create-and-arm an autonomous convergence loop (writes via keyoku's own process)
  belay loop list                                  loop-relevant goals × arm/pause state × counters
  belay loop pause <goal> [--note <text>]          pause the Stop-hook hold (the fall-arrest gate stays active)
  belay loop resume <goal>                         resume a paused loop (re-demands fresh goal_assess)
  belay loop disarm <goal>                         unfocus via keyoku + clear arm state (belay returns to no-op)
  belay propose [--dismiss <id>]                   scan for loop-worthy signals; proposals are advisory, never auto-armed
  belay hook <stop|pre-tool-use|session-start>     (hook commands — wired by install)

Belay continues work WITHIN a session and advises across sessions; it never launches
headless runs and never touches credentials.`);
}
